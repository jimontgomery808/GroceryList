package com.example.josh.grocerylist;


public class GroceryItem
{
    private String name;
    private double cost;

    public String getName()
    {
        return name;
    }

    public double getCost()
    {
        return cost;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setCost(double cost)
    {
        this.cost = cost;
    }
}
